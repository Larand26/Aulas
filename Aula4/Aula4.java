
public class Aula4{
    public static void main(String[] args){
       

        for(int i = 1; i <= 5; i++){
            System.out.println(i + "° vez");
        }

        int i = 1;
        while (i <= 5) {
            System.out.println(i + "° vez");
            i++;
        }

        int i2 = 1;
        do{
            System.out.println(i2 + "° vez");
            i2++;
        }while(i2 <= 5);
        
    }
}